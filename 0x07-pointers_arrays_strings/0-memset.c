#include <stdio.h>

/**
 * _memset - makes memory filled with constant byte
 * @s: pointer
 * @b: bytes to be filled
 * @n: number of bytes
 *
 * Return: str.
 */
char *_memset(char *s, char b, unsigned int n)
{
	unsigned int n;

	for (n = 0; n < n; n++)
	{
		s[n] = b;
	}
	return (s);
}
